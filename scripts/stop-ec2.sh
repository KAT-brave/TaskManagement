#!/bin/bash
# =============================================================================
# EC2 インフラの削除（terraform destroy）
# =============================================================================
# 作成したすべての AWS リソースを削除します。
# EC2・Elastic IP を削除することで課金が止まります。
#
# ⚠️  このスクリプトを実行すると EC2 サーバーのデータはすべて削除されます。
#     アプリケーションのデータを保存したい場合は事前にバックアップしてください。
# =============================================================================

set -e

SCRIPT_DIR="$(cd "$(dirname "${BASH_SOURCE[0]}")" && pwd)"
TERRAFORM_DIR="$SCRIPT_DIR/../terraform"

echo "======================================"
echo "  EC2 インフラ削除"
echo "======================================"
echo ""
echo "⚠️  以下のAWSリソースをすべて削除します:"
echo "  - EC2 インスタンス（データは消えます）"
echo "  - Elastic IP"
echo "  - VPC・サブネット・セキュリティグループ"
echo ""
echo "この操作は取り消せません。"
echo ""

read -p "本当に削除しますか？ (yes/no): " confirm
if [ "$confirm" != "yes" ]; then
  echo "中止しました"
  exit 0
fi

echo ""
echo ">>> terraform destroy を実行します..."
cd "$TERRAFORM_DIR"
terraform destroy

echo ""
echo "======================================"
echo "✅ EC2 インフラを削除しました。"
echo "   AWS の課金が止まります。"
echo "======================================"
